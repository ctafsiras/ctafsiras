import Head from 'next/head';
import React, { useState } from 'react';

const AgeCalculator = () => {
  const [result, setResult] = useState(null)
  const [birthDate, setBirthDate] = useState(null)
  var today = new Date();
  var birthdate = new Date(birthDate);
  var age = today.getFullYear() - birthdate.getFullYear();
  var m = today.getMonth() - birthdate.getMonth();
  if (m < 0 || (m === 0 && today.getDate() < birthdate.getDate())) {
    age--;
  }
  var months = m < 0 ? m + 12 : m;
  var days = today.getDate() - birthdate.getDate();
  if (days < 0) {
    days += new Date(today.getFullYear(), today.getMonth(), 0).getDate();
    months--;
  }
  console.log(days, months, age);
  return (
    <div>
      <Head>
        <title>Age Calculator | Tools | Chowdhury Tafsir Ahmed Siddiki</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="keywords" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <div className="card w-96 bg-base-100 shadow-xl mx-auto">

        <input onChange={(e) => setBirthDate(e.target.value)} type="date" placeholder="Type here" className="input input-bordered input-primary w-full max-w-xs" />
        <div className="card-body">
          <h2 className="card-title">Result</h2>
          {result && result}
          <div className="card-actions justify-end">
            <button onClick={() => setResult(<p>Your age is {age} year {months} month {days} day.</p>)} className="btn btn-primary">Show Now</button>
          </div>
        </div>
      </div>

    </div>
  );
}

export default AgeCalculator;
